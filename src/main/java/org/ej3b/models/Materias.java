package org.ej3b.models;

public class Materias {
    private int id;
    private String nom;
    private int carr;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCarr() {
        return carr;
    }

    public void setCarr(int carr) {
        this.carr = carr;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }
}
